package com.mphasis;

public class ComplexNum {
	
	int real;
	int imag;
	public ComplexNum() {
		super();
	}
	public ComplexNum(int real, int imag) {
		super();
		this.real = real;
		this.imag = imag;
	}
	@Override
	public String toString() {
		return "ComplexNum [real=" + real + ", imag=" + imag + "]";
	}
	
	

}
